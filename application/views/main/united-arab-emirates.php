            <h4 class="mb-4">VAT registration</h4>
            <p>The mandatory VAT registration threshold for UAE businesses is AED 375,000. This means that if your annual taxable supplies have exceeded the mandatory threshold in the past 12 months, or the threshold is expected to exceed in the next 30 days, you are required to register for VAT and comply with all VAT obligations.</p>
            <p>Voluntary registration can be done if your taxable supply is expected to exceed AED 187,500 in the next 30 days.</p>
            <p>Two or more legal persons may apply to form a VAT group to the extent that certain criteria are fulfilled by each member of the VAT group.</p>
            <p>The effect of a VAT group registration is that the members of the VAT group are treated as a single taxable person for VAT purposes. This means that:</p>

            <ul>
                <li>Supplies made between members of the VAT group will be disregarded for VAT purposes and therefore no VAT is chargeable on intra-group transactions;</li>
                <li>Only one VAT “Tax Registration Number” (“TRN”) is issued for use by the group;</li>
                <li>The VAT group submits only one tax return which summarises all supplies and purchases made by group members over the VAT period concerned; and</li>
                <li>One member of the VAT group is appointed as ‘representative member’ of the group.</li>
            </ul>
            
            <p>All of the VAT obligations of the VAT group, and all supplies made and received by it, are deemed to be carried out in the name of the representative member.</p>

            <h4 class="mb-4">Timeline to complete VAT registration</h4>
            <p>Generally, the estimated time to complete the application for the VAT registration by the tax authority is 20 business days from the date the completed application is received.</p>

